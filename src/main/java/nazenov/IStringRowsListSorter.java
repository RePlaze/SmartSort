package nazenov;


import java.util.List;

/**
 * Интерфейс механизма «умной» сортировки.
 */
public interface IStringRowsListSorter {
     /** Сортирует переданный список записей таблицы, где каждая запись представляет собой набор колонок,
     * в соответствии с указанной колонкой, соблюдая следующие правила:
     * 1. Значения null и пустые строки в колонке идут первыми, затем идут непустые значения.
     * 2. Каждая строка разбивается на подстроки следующим образом:
     *    - Выделяются непрерывные максимальные фрагменты строки, состоящие только из цифр.
     *    - Все остальные фрагменты считаются нечисловыми подстроками.
     * 3. При сравнении двух подстрок:
     *    - Если обе подстроки состоят только из цифр, они интерпретируются как целые числа,
     *      при этом меньшее число идет первым.
     *    - Если хотя бы одна подстрока содержит нецифровые символы, сравнение выполняется как строки.
     * 4. Сортировка устойчива и сохраняет исходный порядок неразличимых строк.
     *
     * @param rows        Список записей таблицы, который необходимо отсортировать, например, результат SQL-запроса.
     * @param columnIndex Индекс колонки, по которой нужно провести сортировку.
     */

    void sort(List<String[]> rows, int columnIndex);
}
